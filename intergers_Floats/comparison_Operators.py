#The following are the various comparison operators. Only outputs True or False
equalTo = 3 == 3
print(f"3 is equal to 3? {equalTo}")

notEqualTo = 3 != 3
print(f"Is 3 not equal to 3? {notEqualTo}")

greaterThan = 3 > 2
print(f"Is 3 greater than 2? {greaterThan}")

lessThan = 2 < 1
print(f"Is 2 less than 1? {lessThan}")

greaterThan_EqualTo = 2 >= 1
print(f"Is 2 greater than or equal to 1? {greaterThan_EqualTo}")

lessThan_EqualTo = 21 <= 30
print(f"Is 21 less than or equal to 30? {lessThan_EqualTo}")
