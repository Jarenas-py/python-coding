#The abs() function returns the absolute value of an interger or float.
num = -3
print(abs(num))

#The round() function returns the rounded off value of an interger or float. Take note that without arguments, it would only round to the whole number only.
roundNum = 4.567
print(round(roundNum))

#Add arguments in the form of intergers after the variable containing the value or the value in order to specify until which decimal position would be rounded off.
print(round(roundNum, 2))
